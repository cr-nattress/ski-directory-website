/**
 * Get all ski links
 */
export function getSkiLinks(): SkiLink[] {
  return skiLinks;
}

/**
 * Get ski links filtered by type
 */
export function getSkiLinksByType(type: SkiLinkType): SkiLink[] {
  return skiLinks.filter((link) => link.type === type);
}

/**
 * Filter ski links by multiple criteria
 */
export function filterSkiLinks(
  links: SkiLink[],
  filters: {
    type?: SkiLinkType | null;
    region?: SkiLinkRegion | null;
    audience?: SkiLinkAudience | null;
    query?: string;
  }
): SkiLink[] {
  let filtered = [...links];

  // Filter by type
  if (filters.type) {
    filtered = filtered.filter((link) => link.type === filters.type);
  }

  // Filter by region
  if (filters.region) {
    filtered = filtered.filter((link) => link.regions.includes(filters.region!));
  }

  // Filter by audience
  if (filters.audience) {
    filtered = filtered.filter((link) => link.audience.includes(filters.audience!));
  }

  // Filter by search query
  if (filters.query) {
    const q = filters.query.toLowerCase();
    filtered = filtered.filter(
      (link) =>
        link.title.toLowerCase().includes(q) ||
        link.description.toLowerCase().includes(q) ||
        link.tags.some((tag) => tag.toLowerCase().includes(q))
    );
  }

  return filtered;
}

/**
 * Sort ski links by priority (primary) and title (secondary)
 */
export function sortSkiLinks(links: SkiLink[]): SkiLink[] {
  return [...links].sort((a, b) => {
    // Primary sort: priority ascending (lower = better)
    if (a.priority !== b.priority) {
      return a.priority - b.priority;
    }
    // Secondary sort: title A-Z
    return a.title.localeCompare(b.title);
  });
}

/**
 * Group ski links by type
 */
export function groupSkiLinksByType(
  links: SkiLink[]
): Record<SkiLinkType, SkiLink[]> {
  const grouped: Record<SkiLinkType, SkiLink[]> = {
    'resort': [],
    'snow-weather': [],
    'webcam-trailmap': [],
    'trip-planning': [],
    'gear-reviews': [],
    'education': [],
    'community': [],
    'news': [],
  };

  for (const link of links) {
    grouped[link.type].push(link);
  }

  // Sort each group
  for (const type of Object.keys(grouped) as SkiLinkType[]) {
    grouped[type] = sortSkiLinks(grouped[type]);
  }

  return grouped;
}

/**
 * Get link counts by type
 */
export function getSkiLinkStats(): { total: number; byType: Record<SkiLinkType, number> } {
  const byType: Record<SkiLinkType, number> = {
    'resort': 0,
    'snow-weather': 0,
    'webcam-trailmap': 0,
    'trip-planning': 0,
    'gear-reviews': 0,
    'education': 0,
    'community': 0,
    'news': 0,
  };

  for (const link of skiLinks) {
    byType[link.type]++;
  }

  return {
    total: skiLinks.length,
    byType,
  };
}

/**
 * Get featured links
 */
export function getFeaturedSkiLinks(): SkiLink[] {
  return skiLinks.filter((link) => link.isFeatured);
}

/**
 * Extract domain from URL for display
 */
export function getDisplayDomain(url: string): string {
  try {
    const hostname = new URL(url).hostname;
    return hostname.replace(/^www\./, '');
  } catch {
    return url;
  }
}
